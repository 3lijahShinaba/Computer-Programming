public class Task07 {
    public static void main(String[] args) {
        // Task 7: Nested loop to create a square of asterisks
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
